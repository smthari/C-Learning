#include<stdio.h>

int main() 
{
    int n1 = -1;
    float n2 = 3.142;
    double n3 = 6.123456789;
    char c1 = 'M';
    
    printf("\n Integer datatype   : %i", n1);
    printf("\n Float datatype     : %f", n2);
    printf("\n Double datatype    : %lf", n3);
    printf("\n Character datatype : %c", c1);
    
    return 0;
}